void task1(Protothread_t* pt)
{
    PT_BEGIN(pt);
    static int value = 0;
    while(1){
      PT_WAIT_UNTIL(pt, !TestQueue_IS_EMPTY(&testQueue)); // 等待队列非空
      TestQueue_POP(&testQueue, &value); // 从队列中取出一个
      LOG_DEBUG("Task 1 is running,ticks: %lu,popped value: %d", GET_TICKS(), value);
      // PT_WAIT_TICK(pt, 1000); // 每隔1000毫秒执行一次
    }
    PT_END(pt);
}

void task2(Protothread_t* pt)
{
    PT_BEGIN(pt);
    while(1){
        LOG_DEBUG("Task 2 is running,ticks: %lu", GET_TICKS());
        HAL_GPIO_TogglePin(PCB_LED1_GPIO_Port, PCB_LED1_Pin);
        PT_WAIT_TICK(pt, 1500); // 每隔1500毫秒执行一次
    }
    PT_END(pt);
}

uint8_t ReadKey1Pin(MulitKey_t* key)
{
    return HAL_GPIO_ReadPin(PCB_KEY1_GPIO_Port, PCB_KEY1_Pin);
}

void Key1PressedCallback(MulitKey_t* key)
{
  static uint32_t press_count = 0;
  TestQueue_PUSH(&testQueue, ++press_count); // 模拟按键事件，向队列中添加一个值
  LOG_DEBUG("Key 1 Pressed,ticks: %lu", GET_TICKS());
}

